function renderProjectGrid() {
  const visibleProjects = repoData
    .filter((repo) => !archiveExclusions.has(repo.name))
    .filter((repo) => {
      const category = projectCategoryMap.get(repo.name) || inferCategory(repo);
      const haystack = [repo.name, repo.description, repo.language, ...(repo.topics || [])]
        .filter(Boolean)
        .join(" ")
        .toLowerCase();

      const matchesFilter = activeFilter === "all" || category === activeFilter;
      const matchesSearch = !searchValue || haystack.includes(searchValue);

      return matchesFilter && matchesSearch;
    });

  projectGrid.innerHTML = "";

  if (visibleProjects.length === 0) {
    projectGrid.innerHTML = `
      <article class="project-list-card project-list-card-empty">
        <h3>No matching projects</h3>
        <p>Try a different search term or switch the active filter.</p>
      </article>
    `;
    return;
  }

  visibleProjects.forEach((repo) => {
    const category = projectCategoryMap.get(repo.name) || inferCategory(repo);
    const article = document.createElement("article");
    article.className = "project-list-card";
    article.innerHTML = `
      <div class="project-list-top">
        <div>
          <p class="project-category">${labelForCategory(category)}</p>
          <h3>${repo.name}</h3>
        </div>
        <p class="project-meta">Updated ${formatDate(repo.updatedAt)}</p>
      </div>
      <p class="project-summary">${repo.description || "Repository available on GitHub."}</p>
      <div class="project-list-bottom">
        <div class="chip-grid">
          ${renderRepoChips(repo)}
        </div>
        <a href="${repo.url}" target="_blank" rel="noreferrer">Open repo</a>
      </div>
    `;
    projectGrid.append(article);
  });
}

function renderRepoChips(repo) {
  const chips = [];

  if (repo.language) {
    chips.push(repo.language);
  }

  if (repo.homepage) {
    chips.push("Live page");
  }

  if (repo.stars > 0) {
    chips.push(`${repo.stars} star${repo.stars === 1 ? "" : "s"}`);
  }

  (repo.topics || []).slice(0, 2).forEach((topic) => chips.push(topic));

  return chips.map((item) => `<span class="chip">${item}</span>`).join("");
}

function inferCategory(repo) {
  const description = (repo.description || "").toLowerCase();
  const name = repo.name.toLowerCase();
  const language = (repo.language || "").toLowerCase();

  if (description.includes("commerce") || description.includes("woocommerce") || description.includes("store") || name.includes("woo")) {
    return "commerce";
  }

  if (description.includes("security") || description.includes("privacy") || description.includes("leak") || name.includes("shield")) {
    return "security";
  }

  if (description.includes("chrome") || description.includes("extension") || name.includes("tab")) {
    return "browser";
  }

  if (description.includes("music") || description.includes("audio") || description.includes("midi") || description.includes("vst") || name.includes("synth")) {
    return "audio";
  }

  if (description.includes("ai") || description.includes("automation") || name.includes("ai") || name.includes("grok")) {
    return "apps";
  }

  if (
    description.includes("game") ||
    name.includes("invaders") ||
    name.includes("daze") ||
    name.includes("commando") ||
    name.includes("720") ||
    language === "html"
  ) {
    return "games";
  }

  if (language === "python" || language === "dart" || language === "kotlin" || description.includes("gui")) {
    return "apps";
  }

  if (language === "typescript" || description.includes("saas") || description.includes("api")) {
    return "saas";
  }

  return "all";
}

function labelForCategory(value) {
  switch (value) {
    case "saas":
      return "SaaS and API";
    case "security":
      return "Privacy and Security";
    case "audio":
      return "AI and Audio";
    case "commerce":
      return "Commerce";
    case "apps":
      return "Apps and Tools";
    case "games":
      return "Games and Interactive";
    case "browser":
      return "Browser Product";
    default:
      return "Project";
  }
}

function formatDate(value) {
  if (!value) {
    return "recently";
  }

  return new Intl.DateTimeFormat("en-GB", {
    day: "numeric",
    month: "short",
    year: "numeric"
  }).format(new Date(value));
}
